#include "checker.h"
#include <utils.h>

void	rr(t_stack **a, t_stack **b, char debug__)
{
	if (*a && (*a)->next)
		su_stack__(a);
	if (*b && (*b)->next)
		su_stack__(b);
	if (debug__)
		print_stack(*a, *b, RR);
}
